"""
Python 第 22 课 —— 正则表达式

正则表达式 = 用模式匹配文本。
你的实验数据里 "WA3_0-5cm_TP_1020.5" 这种字符串，
用 split() 能拆，但正则更强大——能同时提取数字、验证格式、替换内容。
"""

import re

# ═══════════════════════════════════════════════════════════
# 1. 基础模式 — 匹配和搜索
# ═══════════════════════════════════════════════════════════

text = "样品 WA3 的 TP 值为 1020.5 mg/kg，采集于 2026-03-15"

# re.search(模式, 文本) → 找第一个匹配
match = re.search(r"WA\d+", text)
#                r"..." = 原始字符串（\ 不会被当成转义）
#                \d = 数字, + = 1个或多个
print(f"找到样品名: {match.group()}")    # WA3

# 找小数
match = re.search(r"\d+\.\d+", text)
print(f"找到小数: {match.group()}")       # 1020.5

# 找日期
match = re.search(r"\d{4}-\d{2}-\d{2}", text)
#                {4} = 恰好4个数字
print(f"找到日期: {match.group()}")       # 2026-03-15

# ═══════════════════════════════════════════════════════════
# 2. 提取 — 用括号 () 分组捕获
# ═══════════════════════════════════════════════════════════

# 从文件名同时提取样品、深度、指标、值
filename = "WA3_0-5cm_TP_1020.5" 

pattern = r"(WA\d+)_(.+?)_(.+?)_([\d.]+)"
#          ↑      ↑     ↑     ↑
#          样品    深度   指标   数值
#          () = 捕获组，之后可以单独取出来

match = re.search(pattern, filename)
if match:
    print(f"\n文件名解析:")
    print(f"  样品: {match.group(1)}")
    print(f"  深度: {match.group(2)}")
    print(f"  指标: {match.group(3)}")
    print(f"  数值: {match.group(4)}")

# ═══════════════════════════════════════════════════════════
# 3. 常用模式速查
# ═══════════════════════════════════════════════════════════
#
#   \d    数字              \w    字母/数字/下划线
#   \s    空白(空格Tab)     .     任意字符
#   +     1个或多个         *     0个或多个
#   ?     0个或1个          {3}   恰好3个         {2,5}  2-5个
#   ^     开头              $     结尾
#   [ABC] A或B或C           [^0-9] 非数字
#   ()    捕获组            (?:)  非捕获组

# ═══════════════════════════════════════════════════════════
# 4. 替换和分割
# ═══════════════════════════════════════════════════════════

# re.sub(模式, 替换成什么, 文本) — 替换
dirty = "TP =  1020.5  ± 45.2   mg/kg"
clean = re.sub(r"s\+", " ", dirty)       # 多个空格→单个空格
print(f"\n去多余空格: '{clean}'")

# re.findall(模式, 文本) — 找所有匹配
records = "WA1:533.5, WA2:517.6, WA3:488.9"
all_psi = re.findall(r"(\d+\.\d+)", records)
print(f"提取所有 PSI 值: {all_psi}")     # ['533.5', '517.6', '488.9']

# re.split(模式, 文本) — 用正则分割
data_line = "WA1  34.85  0.8029  517.6"
parts = re.split(r"\s+", data_line)     # 任意空白分割
print(f"按空白分割: {parts}")

# ═══════════════════════════════════════════════════════════
# 5. 科研实用：验证样品编号格式
# ═══════════════════════════════════════════════════════════

def is_valid_sample(s):
    """判断是否为合法样品编号: WA后面跟1-2位数字"""
    return bool(re.fullmatch(r"WA\d{1,2}", s))

for sid in ["WA1", "WA12", "WA", "wa1", "WA123"]:
    ok = "✅" if is_valid_sample(sid) else "❌"
    print(f"  {sid} → {ok}")


