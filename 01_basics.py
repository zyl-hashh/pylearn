# ═══════════════════════════════════════════════════════════
# 1. print() — 把东西显示到屏幕上
# ═══════════════════════════════════════════════════════════
# 这是你用得最多的函数。括号里写什么，屏幕就显示什么。

print("Hello! 这是我的第一行 Python 代码")
print("中文也能正常显示")

# 打印数字：
print(42)
print(3.14159)

# 打印计算结果：
print(75 + 25)        # 加法 → 100
print(75 - 25)        # 减法 → 50
print(75 * 2)         # 乘法 → 150
print(75 / 2)         # 除法 → 37.5
print(75 ** 2)        # 幂   → 75² = 5625

print("──────────────")


# ═══════════════════════════════════════════════════════════
# 2. 变量 — 给数据起个名字，存起来
# ═══════════════════════════════════════════════════════════
# 变量就像贴了标签的试管，里面装着数据。

x = 10              # 把 10 存到变量 x 里
y = 3               # 把 3 存到变量 y 里

# 你可以像用数字一样用变量：
print("x =", x)
print("y =", y)
print("x + y =", x + y)
print("x * y =", x * y)
print("x / y =", x / y)      # 普通除法，得到小数
print("x // y =", x // y)    # 整除，只留整数部分 → 3
print("x % y =", x % y)      # 取余数 → 10 ÷ 3 = 3余1

# 变量可以重新赋值，新值覆盖旧值：
x = 100
print("现在 x 变成:", x)

print("──────────────")


# ═══════════════════════════════════════════════════════════
# 3. 数据类型
# ═══════════════════════════════════════════════════════════

# Python 有三种最基础的类型：

a = 10                  # int    整数
b = 3.14                # float  小数（浮点数）
c = "WA1"               # str    字符串（文字）

# type() 可以查看变量是什么类型：
print("a 的类型:", type(a))
print("b 的类型:", type(b))
print("c 的类型:", type(c))

# 类型决定了能做什么：
print("整数 + 整数:", 10 + 20)       # 正常加法
print("字符串 + 字符串:", "WA" + "1") # 字符串+是拼接，得到 "WA1"

# 类型转换（这个很常用！）：
num_str = "42"
num_int = int(num_str)      # 字符串 → 整数
print(num_str, "是", type(num_str))
print(num_int, "是", type(num_int))

pi = 3.14159
pi_int = int(pi)            # 小数 → 整数（直接截断，不是四舍五入）
print(pi, "→", pi_int)

print("──────────────")


# ═══════════════════════════════════════════════════════════
# 4. 字符串的花样
# ═══════════════════════════════════════════════════════════

name = "WA1"
value = 533.5

# f-string：在字符串里插入变量（你科研代码里到处都是）
# 在字符串前面加 f，然后 {变量名} 就会被替换成值
print(f"样品 {name} 的 PSI 值是 {value} mg/kg")

# 控制小数位数：
print(f"PSI = {value:.1f}")   # .1f = 保留 1 位小数 → 533.5
print(f"PSI = {value:.2f}")   # .2f = 保留 2 位小数 → 533.50
print(f"PSI = {value:.0f}")   # .0f = 不要小数   → 534

# 你之前代码里看到的都是这个用法：
r2 = 0.8742
p = 0.0031
print(f"R² = {r2:.3f}, p = {p:.4f}")

print("──────────────")


