"""
Python 第 28 课 —— 虚拟环境：不让项目打架

═══════════════════════════════════════════════════════════
你迟早会遇到这种情况
═══════════════════════════════════════════════════════════

项目A 需要 pandas 1.5.0，项目B 需要 pandas 2.0.0。
你全局只有一个 Python，只能装一个版本的 pandas。
装 2.0.0 → 项目A 崩了。装 1.5.0 → 项目B 的 API 变了。

虚拟环境就是给每个项目一个"独立的小房间"——每个房间有自己
的一套 Python 和库，互不干扰。

═══════════════════════════════════════════════════════════
核心概念
═══════════════════════════════════════════════════════════

 全局 Python
   → 你装在 E:\Python3.13\ 的 Python
   → 全局安装的包：pip install xxx 直接装到这里
   → 所有项目共用

 虚拟环境 (venv / virtualenv)
   → 一个文件夹，里面有一套"克隆版"的 Python
   → 激活虚拟环境后，pip install 只装到这个文件夹
   → 不同项目用不同的虚拟环境，互不影响

 为什么重要？
   → 项目A 用 pandas 1.x → 虚拟环境A 装 pandas 1.x
   → 项目B 用 pandas 2.x → 虚拟环境B 装 pandas 2.x
   → 两者互不干扰，同时运行

═══════════════════════════════════════════════════════════
本节操作：请打开终端（CMD 或 Git Bash），跟着敲
═══════════════════════════════════════════════════════════
"""

