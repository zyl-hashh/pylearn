mock_pip_list = """Package    Version
-------------------
pandas     2.0.3
numpy      1.26.4
matplotlib 3.8.0
broken-package  ???          # 这行版本号不对
scipy      1.11.0
requests   2.31.0"""

# ↓↓↓ 你的代码 ↓↓↓
import re
def parse_pip_list(text):
    def parse_version(v):
        try:
            float(v)
            return v
        except ValueError:
            if re.match(r'\d+\.\d+', v):
                return v
            return None
    lines=text.strip().split('\n')
    data=lines[2:]
    result=[]
    for i in data:
        parts=i.split()
        if len(parts) <2:
            continue
        packge=parts[0]
        version=parts[-1]
        checked=parse_version(version)
        if checked:
            result.append({'packge':packge,'version':version})
        else:
            print(f"跳过异常行:{i.strip()}")
    return result
print(parse_pip_list(mock_pip_list))

