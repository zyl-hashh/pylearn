print("""
┌─────────────────────────────────────────────────────────┐
│ 第 1 步：创建虚拟环境                                     │
└─────────────────────────────────────────────────────────┘

在终端里：
  cd ~/Desktop
  mkdir myproject
  cd myproject
  python -m venv venv
  #         ↑     ↑
  #         │     └── 虚拟环境的文件夹名（惯例叫 venv 或 .venv）
  #         └── -m = 运行一个模块（这里运行的是 venv 模块）

这会在 myproject 里创建一个 venv 文件夹，里面有：
  venv/
    Scripts/
      python.exe      ← 这是虚拟环境自己的 Python
      pip.exe         ← 这是虚拟环境自己的 pip
      activate        ← 激活脚本
    Lib/
      site-packages/  ← pip install 的包会装到这里

此时你还没"进入"虚拟环境——只是创建了一个空房间而已。
""")


