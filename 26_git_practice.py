"""
Python 第 26 课 —— Git 实操：代码的时光机

═══════════════════════════════════════════════════════════
为什么要学 Git？
═══════════════════════════════════════════════════════════
你写论文时是不是这样：
  论文初稿.docx → 论文修改版.docx → 论文最终版.docx →
  论文最终真的不改了.docx → 论文打死也不改了.docx

Git 把这个问题一劳永逸地解决了：
  - 每次"保存"都是一个快照，可以随时回到过去
  - 可以看到"上周三改了哪几行"
  - 可以把"实验A分支"和"实验B分支"分开开发，最后合并

═══════════════════════════════════════════════════════════
核心概念（先读三遍，看不懂没关系，实操后会懂）
═══════════════════════════════════════════════════════════

 仓库 (repository)
   → 一个被 Git 管理的文件夹，里面有一个隐藏目录 .git
   → 这个 .git 目录存储了所有历史快照
   → 你的项目文件夹 = 工作目录 + .git 仓库

 工作区 (working directory)
   → 你正在编辑的文件，就是你肉眼看到的那些 .py 文件

 暂存区 (staging area / index)
   → 一个"购物车"——你把要保存的文件先加进来
   → git add 文件名 = 把文件放进购物车
   → 不放进购物车的文件不会被保存

 提交 (commit)
   → 把购物车里的文件拍一张快照，永久保存
   → git commit -m "说明" = 拍照 + 写备注

 为什么分两步（add → commit）？
   → 因为你可能改了 5 个文件，但只想保存其中 3 个
   → add 选你要的，commit 一次性保存选中的

═══════════════════════════════════════════════════════════
本节练习：打开 Git Bash，跟着下面的命令敲
（不要复制粘贴！一个一个敲，看到输出想一下意思）
═══════════════════════════════════════════════════════════
"""

