# ═══════════════════════════════════════════════════════════
# 第 2 步：第一次提交 — git add + git commit
# ═══════════════════════════════════════════════════════════

print("""
┌─────────────────────────────────────────────────────────┐
│ 第 2 步：创建文件 + 第一次提交                            │
└─────────────────────────────────────────────────────────┘

  echo "print('Hello Git')" > hello.py    # 创建一个文件
  git status                               # 查看当前状态

你会看到:  Untracked files: hello.py (红色)
意思：Git 发现了一个新文件，但还没开始跟踪它。

  git add hello.py                         # 把文件加入暂存区（购物车）
  git status                               # 再看一次状态

你会看到:  Changes to be committed: new file: hello.py (绿色)
意思：文件已在购物车里，等待提交。

  git commit -m "第一次提交：创建hello.py"   # 拍照保存！

你会看到:  1 file changed, 1 insertion(+)
意思：快照已保存，备注是"第一次提交：创建hello.py"。

三步走总结：
  git add 文件名     → 挑文件，放进购物车
  git commit -m "备注" → 拍照，永久保存
  git status         → 随时看"还有哪些改动没存"
""")


# ═══════════════════════════════════════════════════════════
# 第 3 步：修改文件 + 再提交 — 体会"历史快照"
# ═══════════════════════════════════════════════════════════

