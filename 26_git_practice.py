logs = [
    {"author": "Nicholas", "message": "修复PSI计算bug"},
    {"author": "Nicholas", "message": "添加EPC0图"},
    {"author": "Alice", "message": "重构数据读取"},
    {"author": "Nicholas", "message": "更新README"},
    {"author": "Alice", "message": "添加单元测试"},
]

# 任务：统计每个作者的提交次数，输出格式：
#   Nicholas: 3 次
#   Alice: 2 次
# 提示：用字典计数器

# ↓↓↓ 你的代码 ↓↓↓
def count_commits(logs):
    counter = {}
    for log in logs:
        author = log["author"]
        if author in counter:
            counter[author] += 1
        else:
            counter[author] = 1
    return counter
commit_counts = count_commits(logs)
for author, count in commit_counts.items():
    print(f"{author}: {count} 次")




