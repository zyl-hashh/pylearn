def factorial_iterative(n):
    """用循环算阶乘: n! = 1×2×3×...×n"""
    result = 1
    for i in range(1, n + 1):
        result *= i
    return result

# 递归版本
def factorial_recursive(n):
    """用递归算阶乘:
    n! = n × (n-1)!
    基线条件: n <= 1 → 返回 1
    递归条件: n > 1 → 返回 n × factorial(n-1)
    """
    print(f"  进入: factorial({n})")        # 看"递"的过程

    if n <= 1:                              # ← 基线条件
        print(f"  到达基线: n={n}，返回 1")
        return 1
    else:                                   # ← 递归条件
        result = n * factorial_recursive(n - 1)
        print(f"  返回: factorial({n}) = {n} × factorial({n-1}) = {result}")
        return result

print("\n循环版: 5! =", factorial_iterative(5))
print("\n递归版(注意看递和归的过程):")
print(f"\n结果: 5! = {factorial_recursive(5)}")

# 拆解 factorial_recursive(5) 的执行过程:
