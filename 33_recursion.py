print("""
规则：3 根柱子，N 个圆盘（大的不能放在小的上面）。
目标：把所有圆盘从 A 柱移到 C 柱。

递归思路（n 个盘）：
  1. 把上面 n-1 个盘从 A 移到 B（借助 C）    ← 交给递归
  2. 把最底下 1 个盘从 A 移到 C             ← 自己做
  3. 把上面 n-1 个盘从 B 移到 C（借助 A）    ← 交给递归

你不需要跟踪每一步——相信递归能完成 n-1 个盘的任务。
这就是递归的思维：把大问题拆成小问题，把细节交给递归。
""")


def hanoi(n, source, target, auxiliary):
    """汉诺塔：打印每一步移动。

    n: 盘子数量
    source: 起始柱
    target: 目标柱
    auxiliary: 辅助柱
    """
    if n == 1:                                         # 基线：只有一个盘
        print(f"  盘子 1: {source} → {target}")
        return
    # 递归：上面的 n-1 个盘
    hanoi(n - 1, source, auxiliary, target)            # 第 1 步
    print(f"  盘子 {n}: {source} → {target}")           # 第 2 步（自己）
    hanoi(n - 1, auxiliary, target, source)            # 第 3 步


