"""
Python 第 33 课 —— 递归与分治

═══════════════════════════════════════════════════════════
递归是什么？
═══════════════════════════════════════════════════════════

递归 = 函数调用自己。

你肯定觉得奇怪——函数调用自己不是死循环吗？不会，
因为每次调用的数据规模都变小了，最终会到达一个
"不再调用自己"的终点（称为 基线条件 base case）。

═══════════════════════════════════════════════════════════
递归需要两个条件（缺一不可）
═══════════════════════════════════════════════════════════

  1. 基线条件 (base case)
     → 什么时候停止递归？（递的终点）

  2. 递归条件 (recursive case)
     → 我怎么把问题变小，然后继续递归？（归的路径）

没有基线条件 → 无限递归 → 栈溢出（RecursionError）
没有递归条件 → 函数就执行一次，不叫递归

═══════════════════════════════════════════════════════════
怎么理解递归？想象你在电影院问前面的人"这是第几排？"
═══════════════════════════════════════════════════════════

 你问第一个人，他说"不知道，我问前面的"
 → 第二个人说"不知道，我问前面的"
 → ...
 → 第 n 个人（第一排）说"我是第 1 排"  ← 基线条件到了
 → 第 n-1 个人说"那我就是第 2 排"     ← 开始"归"
 → ...
 → 第一个人说"那你就是第 n 排"         ← 得到答案

递归的过程 = 把问题不断"递"出去，到达终点后再一层层"归"回来。
"""

