print("""
分治 = 把大问题分成两个小问题，各自解决，合并结果。

经典例子：归并排序 (Merge Sort)

归并排序的步骤：
  1. Divide: 把数组从中间分成两半
  2. Conquer: 递归地对每一半排序
  3. Combine: 把两个有序子数组合并成一个有序数组

时间复杂度: O(n log n) — 最优！
空间复杂度: O(n) — 需要额外空间存放合并结果
""")


def merge_sort(arr):
    """归并排序。

    比如排 [38, 27, 43, 3, 9, 82, 10]：
      Divide: [38,27,43,3] [9,82,10]
      Divide: [38,27] [43,3] [9,82] [10]
      Divide: [38] [27] [43] [3] [9] [82] [10]  ← 都只剩一个元素
      Merge:  [27,38] [3,43] [9,82] [10]        ← 开始合并
      Merge:  [3,27,38,43] [9,10,82]            ← 继续合并
      Merge:  [3,9,10,27,38,43,82]              ← 最终结果
    """
    # 基线条件：只有一个元素或空列表
    if len(arr) <= 1:
        return arr

    # Divide: 分成两半
    mid = len(arr) // 2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])

    # Conquer + Combine: 合并两个已排序的列表
    return merge(left, right)


def merge(left, right):
    """合并两个已排序的列表"""
    result = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    # 把剩余的直接追加（最多只有一个列表还有剩余）
    result.extend(left[i:])
    result.extend(right[j:])
    return result


