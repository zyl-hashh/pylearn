print("""
斐波那契数列: 1, 1, 2, 3, 5, 8, 13, 21, 34, ...
规则: 每个数 = 前两个数之和
  fib(1) = 1, fib(2) = 1
  fib(n) = fib(n-1) + fib(n-2)
""")


call_count = 0

def fib_recursive(n):
    """纯递归——指数时间复杂度 O(2ⁿ)，n=40 就非常慢了"""
    global call_count
    call_count += 1
    if n <= 2:
        return 1
    return fib_recursive(n - 1) + fib_recursive(n - 2)


def fib_memo(n, memo=None):
    """带记忆的递归——O(n)，飞一样的速度

    思想：用字典记住已经算过的结果，避免重复计算。
    这是"动态规划"的雏形——面试标配。
    """
    if memo is None:
        memo = {}
    if n in memo:
        return memo[n]
    if n <= 2:
        return 1
    memo[n] = fib_memo(n - 1, memo) + fib_memo(n - 2, memo)
    return memo[n]


